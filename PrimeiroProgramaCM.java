package primeiroprograma; // É opcional quando for fazer o primeiro programa.
// Isso "{} é uma chave, é importante ajustar as abertas '{' com uma '}' ao final para fechar".
// As chaves na maioria das linguagens são blocos, tudo que está em seu interior é considerado um bloco/tijolo.
public class PrimeiroPrograma{
    //Dentro do bloco da classe (class) vamos criar um método, que é o método principal (main).
    public static void main (String [] args){
        // Bom, até aqui temos um método PRINCIPAL/MAIN dentro da CLASSE/CLASS "PrimeiroPrograma",
        //dentro do PACOTE/PACKAGE "primeiroprograma".
        System.out.print("Olá, Mundo!"); //println pula uma linha, print não pula linha.
        System.out.println("Olá, Mundo!"); //println pula uma linha, print não pula linha.
        // Nomenclatura de Pacotes: Seus nomes devem ser sempre em letras minúsculas. Se o nome do pacote for composto por múltiplas palavras,
        //elas devem ser concatenadas sem espaços ou hífens, mas com ou sem pontos. Exemplo: primeiroprograma, utilitarios.ajudantes.

        // Nomenclatura de Classes: Seus nomes devem usar o estilo PascalCase. A primeira letra de cada palavra no nome da classe deve ser maiúscula,
        //e não há espaços entre as palavras. Exemplo: PrimeiroPrograma, MinhaPrimeiraClasse, CalculadoraDeImpostos, ContaCorrente.

        // Se a letra inicial da 1° palavra for minúscula e do resto CamelCase, é um atributo, variável ou método, e não uma classe.

    }
}
